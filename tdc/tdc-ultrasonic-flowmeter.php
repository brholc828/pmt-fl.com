				<p>The Time-of-Flight (also Transit-time) method uses the change in sonic velocity with and against the direction of flow. It is: v = d*(t2-t1)/(2*t1*t2) = ca. c2/(2d*cosb)*(t2-t1) That means: high resolution demands high precision time-measurement - especially for small flow velocities. Take an example: we want to meassure a flow of 0,5m/s in water (c=1480m/s at 20&deg;C) with a resolution of0.5%. The distance between the two ultrasonic transducers may be 10cm at an angel of 45&deg;. The final time difference is then 33ns.  The needed resolution is 165ps - for the TDC-GP2 with its 65 ps single shot resolution this is no problem!</p>
 
