					<h2 class="sub">Electromagnetic Interference (EMI):</h2>
					<p>All the DLC types introduced here were tested on a prototype-basis towards EMI (10V/m) and showed very good results. Thanks to the design of a digital load cell in general (electronics encapsulated in the load cell body) the interference level always stays well within 1e of the relevant specification. Therefore a DLC is a good solution for electromagnetically noisy environments. (See figures below.)</p>
					
					<h2 class="sub">DLC Development Kit</h2>
					<p>For an easy entry into DLC design and a fast development acam provides a dedicated digital load cell PCB. The PCB comes together with an entire development kit, which also includes one load cell already equipped with the PCB to show full functionality by ‘plug-and-play’, but there is also a separate PCB to connect your own sensor.</p>
 
					<p>The PCB offers many possibilities for evaluation and design-in, e.g. it is equipped with an external EEPROM for program development and several types of sensors can be connected, e.g. a half-bridge, a classical Wheatstone bridge or the PICOSTRAIN full-bridge.</p>
				
